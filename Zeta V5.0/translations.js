function changeLanguage(lang) {
    localStorage.setItem('zeta_lang', lang);
    applyTranslations(lang);
}

function applyTranslations(lang) {
    const defaultLang = 'th';
    const selectedLang = lang || localStorage.getItem('zeta_lang') || defaultLang;
    
    // Switch lang attribute
    document.documentElement.lang = selectedLang;
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[selectedLang] && translations[selectedLang][key]) {
            el.innerHTML = translations[selectedLang][key]; // Using innerHTML to support <br>
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[selectedLang] && translations[selectedLang][key]) {
            el.setAttribute('placeholder', translations[selectedLang][key]);
        }
    });
    
    // Update active state of language selector buttons if they exist
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if(btn.dataset.lang === selectedLang) {
            btn.style.fontWeight = 'bold';
            btn.style.color = 'var(--primary)';
        } else {
            btn.style.fontWeight = 'normal';
            btn.style.color = 'var(--text-muted)';
        }
    });
}

document.addEventListener('DOMContentLoaded', () => {
    applyTranslations();
});
